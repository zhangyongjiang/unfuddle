package org.gaoshin.openflier.model;

public class Fcircle extends Fshape {
	private long color;
	private int width;
	private int radius;

	public long getColor() {
		return color;
	}

	public void setColor(long color) {
		this.color = color;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getRadius() {
		return radius;
	}

	public void setRadius(int radius) {
		this.radius = radius;
	}
}
